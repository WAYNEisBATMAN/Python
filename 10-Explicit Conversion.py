#2)Explicit conversion--
"""In the explicit/cast type conversion, programmer converts one data type into
   another data type. 
 ->Which can be b/w any of the following types:
 ->int,float,complex,str,list,tuple,bin,oct,hex."""

#Python code for explicit conversion.
 
#initializing string 
x="WAYNEEE"

#Printing string converting to tuple
C=tuple(x)
print("Given string conversion to tuple is: ",end='')
print(C)

C=set(x)
print("Given string conversion to set is: ",end='')
print(C)

C=list(x)
print("Given string conversion to list is: ",end='231465')
print(C)

"""Python’s print() function comes with a parameter called ‘end’. 
   By default, the value of this parameter is ‘\n’, i.e. the new line character. 
   You can end a print statement with any character/string using this parameter."""






